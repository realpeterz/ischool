	<h2>Career Services Mission:</h2>
	<p>
		The iSchool Career Services Office is a collaborative partnership with faculty and staff to empower students to achieve their dreams beyond academics.
	</p>
	<p>
		Our goal is to prepare students for the professional world. To that end, we provide education on the vast array of career paths in information studies, individualized coaching sessions, as well as connections with resources, people, and opportunities.
	</p>
	<p>
		In addition to direct student services, we work with employers to facilitate their success in recruiting iSchool talent. We actively market the skill sets of our students and alumni to employers and seek to attract new organizations, especially emerging and non- traditional industries that may be less familiar with the value of an iSchool education.
	</p>
	<p>
		For specific information please view the menu to the left of this page. If you cannot find what you need, or have any questions, please <a href="http://www.ischool.utexas.edu/careers/contact.php"> contact us</a>.
	</p>
	<p>
		<a href="http://www.ischool.utexas.edu/people/alumni_profiles.php">Alumni Career Profiles</a>: Check out stories from our alumni and be inspired by their successes. This could be you!
	</p>
	<p>
		Graduates from the iSchool develop careers in almost every industry including education, high tech, government, business, and law.
	</p>
	<p>
		The diversity and range of our alumni is impressive when you consider that in one small sampling we have user experience researchers, archivists, information architects, corporate, academic, and public librarians, digital resource managers, and systems analysts. The job titles for the field of information are rapidly evolving and some of the alumni you see profiled here are the first to hold their position within their organization.
	</p>
	<p>
		We educate and encourage our students to create new frontiers while preserving the past, managing the present and designing the future.
	</p>
