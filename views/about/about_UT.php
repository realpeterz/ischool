<h2>ABOUT UT AND AUSTIN </h2>
<h2>The University of Texas</h2>
<p>The University of Texas at Austin is the largest component of the University of Texas System. UT Austin is a major research university that is home to over 48,000 students; 2,700 faculty; and 17,000 staff members.<a href="http://www.utexas.edu/about-ut/"> Learn more about UT. </a></p>
