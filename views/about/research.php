			<h1>
						RESEARCH AND PUBLICATIONS						</h1>
<p>Faculty and student research at the iSchool aims to advance knowledge in key areas of information studies and apply it to the design, creation, use, management, and retrieval of information resources and services. Our diverse faculty has expertise ranging from library and information science to technical fields (computer science, mathematics, chemistry, engineering), to social sciences (psychology, anthropology, linguistics, history, political science, communication, organization studies) to humanities (literature, philosophy, film studies) to professional disciplines (education, business, law).  Their work bridges these disciplines to develop theories, models, and applications in the following broad areas:</p>

<ul>
<li>Digital Libraries</li>
<li>Human-Computer Interaction & Information Architecture</li>
<li>Organization and Retrieval of Knowledge and Information</li>
<li>Preservation and Conservation of Physical and Digital Artifacts</li>
<li>Development, Management, and Evaluation of Collections and Services</li>
<li>Cultural Heritage Development and History and Management of the Cultural Record</li>

<li>Information Policy, Ethics, and Advocacy</li>
<li>User Behavior</li>
<li>Health Informatics</li>
<li>Information Work and Workers</li>
<li>Social Informatics and Digital Media</li>
</ul>

