<h3>Entertainment Expense Reimbursement</h3>

<p>Entertainment expense includes cost of food, beverages, catering for official university occasion, meeting, conference, and workshop.  To request reimbursement for approved entertainment expense, submit to Kathleen Adrian:</p>
<ul>
<li>Original itemized paid receipts</li>
<li>Completed <a href="http://www.utexas.edu/business/accounting/hbp/forms/occasion.pdf">Official Occasion Expense Form (pdf)</a></li>

</ul>
<p>Please consult the <a href="http://www.utexas.edu/business/accounting/hbp/09_expend/expend2-1.html">UT Handbook of Business Procedures</a> for more information.</p>


