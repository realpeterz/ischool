<h2>IT LAB PRINTING</h2> <!-- use h2 for all headings please -->

<p>The School of Information offers several printing options for enrolled students.</p>

<h2>Pricing</h2>

	<ul>
		<li>Black & White printing: $0.08 / page</li>
		<li>Color Printing: $0.16 / page</li>
		<li>CD/DVD Printing: $2.00</li>
		<li>Plotter Printing: ~$.50 / square foot</li>
	</ul>

<h2>Print Cards</h2>

<p>Print cards can be purchased at the vending machine located in the IT Lab. Print cards cost $1.00 and contain 13 $0.08 areas used for printing. Print cards are presented and marked by lab staff when acquiring print-outs.</p>

<p>Patrons are expected to pay for printing at the time of purchase.</p>

<h2>Faculty and Staff</h2>

<p>Faculty and staff can print to any of the IT Lab printers using an office account. Faculty and staff should note the amount printed and sign the provided sheet.</p>

<h2>Plotter Instructions</h2>

<p>Please contact a lab staff member for information about using the plotter.</p>

<h2>Contact</h2>

<p>For more information, please contact a <a href="mailto:itlab-tech@ischool.utexas.edu">lab staff member</a> or the <a href="mailto:support@ischool.utexas.edu">lab manager</a>.</p>

