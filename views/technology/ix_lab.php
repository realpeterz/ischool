<h2>IX LAB</h2> <!-- use h2 for all headings please -->

<p>The Information eXperience Lab spans two rooms at the UT Administration Building (UTA), UTA 5.510 and UTA 5.516, and can be used for usability studies using Morae.</p>

<h2>Location</h2>

<p><a href="http://www.utexas.edu/maps/main/buildings/uta.html">UTA</a> 5.510 &amp; 5.516 | <a href="http://www.ischool.utexas.edu/images/uta_fifth_floor_map.jpg">Fifth Floor Map</a></p>

<h2>Our Purpose</h2>

<p>The Information eXperience Lab is a testing facility dedicated to the
	science of information studies, the empirically-based design of
	information resources, and the education of graduate students in the
	process of both.</p>

<p>In the lab faculty and students conduct experiments on human-information
	processing and usability, accessibility, and other studies of the
	interaction between humans and information sources.</p>

<h2>Who may use the IX Lab?</h2>

<p>School of Information students and faculty will receive priority in scheduling studies.  We welcome other University of Texas researchers' use of the lab.</p>

<h2>Prerequisite</h2>

<p>The most important tenet of testing human subjects is the ethical
	treatment of test participants, including their informed consent. All
	researchers using the IX Lab must first go through the online tutorial
	for using the lab.</p>

<p>For more information, please contact the Director of the Information eXperience Lab, <a href="http://www.ischool.utexas.edu/people/person_details.php?PersonID=52">Randolph Bias</a>.</p>

