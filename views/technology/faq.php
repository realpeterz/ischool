	<p><strong><a name="PineReadOnly"></a>My Pine inbox is read-only and won't let me delete messages. </strong>[ <a href="#top">back to top</a> ]</p>

	<p>Chances are that you have another Pine process already running. Whenever you run Pine, it locks your inbox so that other programs cannot alter the contents while Pine is running. Unfortunately, if Pine is exited in an improper fashion, it refuses to relinquish its lock on the inbox. Thus, the next time you start Pine, it can only open your mail read-only. To remove the lock, you will have to kill the Pine process. In order to do this, you must first telnet into your account on the School of Information server (<a href="/technology/tutorials/start/account/accessing_pc.html">PC</a> | <a href="/technology/tutorials/start/account/accessing_mac.html">Mac</a>). Once at the UNIX prompt, type: <code>ps -ef |grep login</code>, where login is your login name. That tall skinny character in the middle of that line is called the pipe. In general, it is located just above the return key on your keyboard. So, for instance, if my login name is shanew, I would type:0 <code>ps -ef |grep shanew</code> at the prompt. That would return something resembling the following:</p>

	<p>	<code>	shanew &nbsp 29461 &nbsp  &nbsp 1  &nbsp  1 &nbsp 12:32:03  &nbsp ?  &nbsp  &nbsp  &nbsp&nbsp 0:00 &nbsp  pine<br />

				shanew &nbsp 5515   &nbsp 5512  &nbsp 0  &nbsp 14:21:22 &nbsp pts/14  &nbsp 0:00  &nbsp -bash</code></p>

	<p>Your results will undoubtedly vary, but as long as it looks similar to this,
you've probably done the right thing. Now, this display can be more than a little
intimidating, so let's explain what all that stuff is. The last column is the
name of the process being described. Appropriately enough, the first column is
the user name of the person who ran that process. Those two are relatively obvious,
the rest are not. Each process running on a Unix machine has a unique process
id so that the operating system can easily identify which process is which. This
is confusing for humans, but much easier for computers. The second column lists
the process ID (or PID) of the procesc named at the end. The third co,umn indicates
what other process is in control of, or responsible for, the listed process.
The next columns tell us Whether the process is currently running, when it was
started, which terminal it is currently displayin' on, and how mu#h processor
time it has used. </p>
<p>So, how does all this relate to getding our inbox back to normal? Well, in order
to get thin's back to normal, we have to kill the Pine process that won't let
go of the lock. In the example above, we can see that the first line lists a
Pine process with a PID of 29461. You will also note that its controlling process
is 1, which means that the operating itself has control of the process. This,
in addition to the fact that it lists no display terminal (note the ?), tells
us this is the process we need to get rid of. In order to do that, type: <code><strong>kill
-9 <em>PID</em></strong></code> where <em>PID</em> is the process ID listed in the second
column. Thus, in our example, I would type: <code><strong>kill -9 29461</strong></code> at
the <code>bash$</code> prompt. </p>

<p>Just to make sure we really got rid of the process, we can retype the first command, <code><b>ps
-ef |grep <i>login</i></b></code>, where <i>login</i> is your login name. Now,
we should have one less process listed (and probably only one left): </p>
	<p><code>shanew &nbsp 5515 &nbsp 5512 &nbsp 0 &nbsp 14:21:22 &nbsp pts/14 &nbsp 0:00 &nbsp -bash</code></p>

	<p><strong>NOTE:</strong> Misbehaving Pine processes are often caused when your connection to the School of Information server is unexpectedly interrupted, but it can also happen if you accidentally hit Ctrl-Z and suspend your Pine process. If this has happened, there are two things you can do. First, right after you hit Ctrl-Z, you should get a bash$ prompt. If you type <code>fg</code> at this prompt, you should be immediately returned to Pine. In addition, when you first start Pine, you can type 'S' to change your setup, type 'C' for the config screen and receive a list of options you can set. If you page down several screens you will see an option that reads:
	</p>
	<p><code>&lt; &gt; Enable Suspend</code></p>

<p>If there is as X between the &lt; &gt;, you should hit 'X' to unmark that option, type 'E' to exit setup, and type 'Y' to confirm your change. This should prevent Ctrl-Z from throwing you out of your Pine process in the future.</p>

