<h2>Printing</h2>
<p><strong><a name="Print"></a>How do I print? </strong>[ <a href="#top">back to top</a> ]</p>
<p>To print, simply go to File and then Print within the application that you are using. The default printer (that is chosen automatically) is the HP LaserJet 9000 PCL, which prints in black and white. To print in color, choose Suburban (or Dell Color Laser on a Mac). All printouts are sent to the printers located behind the Lab staff desk. You are responsible for paying for every printout that you make, even ones that are made in error.</p>
<p><strong><a name="Pay"></a>How do I pay for printouts? </strong>[ <a href="#top">back to top</a> ]</p>

<p>Print cards are sold in $1 denominations, and may be bought from the stamp machine, located beside the Lab scannex, directly across from the Lab staff desk. If you have four quarters, insert them into the slots provided, and retrieve the print card. The cardboard containers for the print card are recycled - please leave them in the wooden box on the shelf behind the tutorial handouts spinner. Lab staff cannot handle the money or provide change, so please plan accordingly. A change machine is located on the second floor. </p>

<p>Black-and-white prints are approximately $0.08 per print (or one mark on the print card) and color prints are approximately $0.16 per print (two marks on the print card). Double-sided prints cost the same as single-sided ones. Each print card contains 13 marks.</p>
<p><strong><a name="PCLPrint" id="PCLPrint"></a>I just bought a print card in PCL. Can I use it here? </strong>[ <a href="#top">back to top</a> ]</p>
<p>Unfortunately, the print cards in the IT Lab are School of Information specific. We do not accept the print cards sold in PCL or elsewhere on campus. However, the printouts are cheaper here ($0.08 versus $0.13).</p>
<p><strong><a name="OwnPaper"></a>How do I print on my own paper? </strong>[ <a href="#top">back to top</a> ]</p>

<p>Inform the Lab staff that you want to print on your own paper and they will assist you. Please note that we do not operate like a copy-center and our printers will only handle certain types of paper. If you're printing something elaborate (i.e. for a multimedia class) you may want to consider a commercial copy center.</p>

<p><strong><a name="PrintPortion"></a>How do I print out a portion of a web page? </strong>[ <a href="#top">back to top</a> ]</p>
<p>If you're using a PC and Internet Explorer, highlight the area you want to print, go to File, then Print, and choose &quot;selection&quot; under print range. You may be able to copy and paste certain portions of a Web page into an office application program such as Microsoft Word or Open Office. </p>
<p><strong><a name="PrintLaptop"></a>Can I print from my laptop? </strong>[ <a href="#top">back to top</a> ]</p>

<p>No, your laptop is not connected to our print server. You will need to use the computers in the IT Lab in order to print.</p>
