<p><strong><a name="Twice"></a>Why are there twice as many computers as monitors in the IT Lab annex? </strong>[ <a href="#top">back to top</a> ]</p>
<p>Each monitor is hooked up to both a PC and a Mac. A converter box is attached to each station. When the dial on the converter box is on &quot;A&quot;, the computer is a Mac. When the dial on the converter box is turned to &quot;B&quot;, the computer is a PC.</p>

<p><strong><a name="WhereMacs"></a>Where are all the Macs? </strong>[ <a href="#top">back to top</a> ]</p>
<p>Five Macs are located on the right-hand side of the Lab as you enter. If you'd like a little more privacy, feel free to use one of the 12 Macs located in the annex.</p>
<p><strong><a name="NoWireless"></a>I don't have a wireless card in my laptop. Can I connect my laptop to an Ethernet port? </strong>[ <a href="#top">back to top</a> ]</p>
<p>Unfortunately, for security reasons, we do not have any Ethernet ports available in the IT Lab. Ethernet ports are available for laptops at the Perry-Castaneda Library (PCL). More information can be found on the <a href="http://www.lib.utexas.edu/pcl/computers/eic/">PCL Web site</a>. </p>

<p><strong><a name="Dark" id="Dark"></a>Why is it so dark in here? </strong>[ <a href="#top">back to top</a> ]</p>
<p>Certain members of the Lab staff occasionally find themselves overly sensitive to fluorescent light. If it's too dark for your liking, feel free to turn the lights on. The light switch is located next to the Emergency Exit.</p>
<p><strong><a name="MissingFiles" id="MissingFiles"></a>I logged off my machine, and when I logged back in, the files I saved on the desktop were missing! </strong>[<a href="#top">back to top</a> ]</p>
<p>Any files saved to hard drives of most of these computers are erased as soon as you log out. Don't learn this lesson the hard way! Make sure to save all of your work (such as on a disk, in your iSchool server space, or on Webspace) before logging out.</p>

<p><strong><a name="Lock"></a>Can I lock a machine to go to lunch/library/class? </strong>[ <a href="#top">back to top</a> ]</p>

<p>It's a good idea to lock your workstation if you're taking a break. However, if you're going to be gone for longer than 20 minutes, please save your work (such as on a disk, in your iSchool server space, or on Webspace) and log out of the computer. If you do need to leave the workstation locked for a longer period of time, you MUST tell the Lab staff. </p>
<p><strong><a name="TeachMe" id="TeachMe"></a>Can someone teach me how to use this computer/scanner/external device? </strong>[ <a href="#top">back to top</a> ]</p>
<p>Of course! That's what the TAs in the Lab are for. Feel free to ask them any question.&nbsp; The TAs can get you started using the program, answer specific questions, and point out any reference works that are available in the Lab. They cannot offer suggestions on how to do the assignment, sit with you for hours teaching you the software, or do any part of the assignment for you. If you or your classmates feel the need for extended instruction in a particular software program, you may wish to <a href="/technology/instruction/short_courses.php">request a short course</a> or a one-on-one session, or ask your professor to cover it in more depth in class. </p>

<p><strong><a name="Prompt"></a>I want to use a computer, but there's a login prompt. Now what do I do? </strong>[ <a href="#top">back to top</a> ]</p>

<p>The PCs in the IT Lab require you to log in with your UTEID and password.</p>
<p><strong><a name="Download"></a>I want to use an application that is not installed on the Lab computers. Can I download and install it? </strong>[<a href="#top">back to top</a> ]</p>
<p>If you require the use of software not found on the computer, please contact a member of the Lab staff. In many cases, you will be allowed to install it. Keep in mind that the PCs will delete the software when you log off the computer. </p>

