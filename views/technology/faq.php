<p><strong><a name="Permissions"></a>What permissions do I set for my Web pages/directories and why? </strong>[ <a href="#top">back to top</a> ]</p>

<p>You must set permissions for your Web pages to prevent other computer users from potentially browsing through, changing, or corrupting your files.</p>
<p>At the UNIX prompt in your School of Information account, type the following commands: </p>
<p><code>chmod go+x $HOME </code></p>
<p><code>chmod go+rx $HOME/public_html </code></p>
<p>The first command makes your home directory group and world executable. The second command makes your public_html directory group and world readable and executable. All files that you put in your public_html directory must be group and world readable. To make them this way, at the $ prompt in your School of Information home directory, type the following command: </p>

<p><code>chmod go+r public_html/* </code></p>
<p>Each time you add new files to your public_html directory, reissue this command.</p>
<p>If you look at the list of files in your public_html directory, you should see the permissions for each file like this: </p>

<p><code>-rw-r--r-- ... foo.html </code></p>
<p>The first character indicates whether or not it's a directory: Directories have 'd' as the first character and files have '-' as the first character. The next three characters are the read (r), write (w), and execute (x) permissions for yourself. The last six characters are the permissions for your designated group (essentially School of Information account holders) and then the permissions for the rest of the world. So, this means that foo.html is a file, which only you can read and change, and which everyone else can read.</p>
<p>You can also check your permissions within your secure file transfer program. Using SSH Secure File Transfer, right-click on a file, choose Properties, and make sure that each &quot;Execute&quot; column is checked. Using Fugu on a Mac, right-click on the file, choose &quot;Get Info&quot;, and ensure that each &quot;Execute&quot; column is checked. </p>

