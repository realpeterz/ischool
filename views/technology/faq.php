<p><strong><a name="GIFJPEG"></a>What is the difference between GIF and JPEG? </strong>[ <a href="#top">back to top</a> ]</p>

<p>A JPEG (or jpg -- pronounced &quot;j-peg&quot;) file is a format used for images that are photographs or complex pictures. Generally, these images have more than the normal palette of 256 colors. Most images you scan will need to be saved as .jpg files if they are going to be placed online.</p>
