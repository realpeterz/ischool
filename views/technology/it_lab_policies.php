<p><a name="food"></a></p>

<h2>Food and Drink</h2>
                                                                             
<p>Food is not allowed at the computer tables.</p>
                                                                                
<p>Certain drink containers are allowed at the computer tables. These containers include drinks with twist-off lids and spill-proof containers.</p>
                                                                                
<p>If you are found with food or drink in inappropriate areas, you will be asked to move to an 
appropriate location or to leave your items outside of the lab. Further violations will result in 
a revocation of lab privileges.</p> 

