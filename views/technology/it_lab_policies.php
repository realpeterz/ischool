<h2>Lab Equipment</h2>

<p>The IT Lab makes several types of equipment available, primarily funded by student fees.</p>

<p>Checkout equipment is available for reservation by iSchool students, faculty and staff. Priority is given to student use. Please see <a href="index.php?subsite=technology&page=equipment" data-page="equipment">Equipment Reservation</a> for additional information.</p>

<p>Faculty-Staff Equipment is available for reservation by iSchool faculty and staff. IT Lab or full-time technology staff may assist in setting up or delivering this equipment. Please see <a href="index.php?subsite=technology&page=equipment" data-page="equipment">Equipment Reservation</a> for additional information.</p>

<p>Lab-Use Only equipment is available for use by iSchool students, faculty, and staff. There may be restrictions or requirements related to use of particular resources. Please see <a href="index.php?subsite=technology&page=equipment" data-page="equipment">Equipment Reservation</a> page for additional information.</p>

