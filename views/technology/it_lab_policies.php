<p><a name="computer"></a></p>

<h2>Computer lifecycles</h2>

<p>Lab computers are generally replaced on a 2-year cycle. PCs that were in the lab are cascaded to the Computer Classroom. PCs that were in the computer classroom are cascaded to other areas in the department.</p>

