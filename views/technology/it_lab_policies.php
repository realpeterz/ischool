<p><a name="#general"></a></p>

<h2>General Rules</h2>

    <ul>
        <li>Absolutely no food or tobacco products are allowed within the facility.</li>
        <li>Drinks must be in approved, spill-proof containers.</li>
        <li>Work quietly. Don't disturb other users. </li>
        <li>You can be logged in on only one workstation at a time. You must use  your own user identification. Any attempt to bypass security can result in loss  of privileges. </li>
        <li>Do not violate copyright law or school software licenses.</li>
        <li>You must log out of a workstation before leaving the facility. Do  not leave your workstation unattended. </li>
        
        <li>If you intend to listen to audio (CDs. MP3s, etc.), you must use headphones (with 1/8" jack). We have several types of headphones available for use in the Lab, or you can use your own.</li>
        <li>Wheelchair users have priority for use of the two &quot;adjustable&quot;  workstations; others may use these stations, but if you are not in a  wheelchair, you will be asked to give up the station if it is needed.</li>
        <li>Do not remove lab materials, such as manuals, from the facility without  explicit permission from the lab staff.</li>
        <li>If you are checking out AV materials or laptop computers, give yourself at least 15 minutes to complete the check-out procedure. </li>
        <li>Clean up after yourself.</li>
    </ul>

