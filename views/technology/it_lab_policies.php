<p><a name="course"></a></p>

<h2>Course Reserves</h2>

<p>A course reserves cabinet is available in the IT Lab reference room.</p>

<p>The IT Lab and technology staff do not maintain the reserves cabinet and are not responsible 
for items placed in the cabinet.</p>

<p>Maintenance of course materials found in the reserves cabinet is the responsibility of the 
faculty member using the cabinet.</p>

<p>At this time, materials can be checked out for 2 hours.</p>

<p>Students are expected to use the form located at the reserves cabinet when removing materials.</p>

<p>For more information, please contact the <a href="mailto:support@ischool.utexas.edu">IT Lab 
Manager</a>.

