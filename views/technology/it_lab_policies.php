<p><a name="purchasing"></a></p>

<h2>Purchasing</h2>

<p>The IT Lab and equipment are funded through different means, with the bulk of funding coming from student fee money.</p>
<p>Students can request the purchase of hardware or software by sending a message to the IT Lab mailing list. Purchased items must be used to directly contribute to student education.</p>

<p>Not all requests can be purchased; priority is given to materials used in classes.</p>

