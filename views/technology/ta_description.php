<h1>TA DESCRIPTION OF ROLES AND RESPONSIBILITIES</h1> <!-- use h2 for all headings please -->

<h2>Purpose</h2>

<p>Students appointed to a Teaching Assistant (TA) position in the School of Information are to assist a faculty member with his or her instructional responsibilities. Such appointments provide an opportunity for students to develop mentoring relationships and specialized research or technology skills.</p>

<h2>TA Pool</h2>

<p>Teaching Assistantships are considered a financial award, and for this reason students must meet certain academic standards of maintaining a 3.5 GPA and not have any incompletes.</p>

<p>The Admission and Awards Committee selects iSchool students to be part of a TA pool, from which faculty can select their TA. Preference is given to iSchool students. Certain exceptions will be made if a faculty member is teaching a course or conducting research that requires a special skill set.</p>

<p>Faculty may expect assistance before the formal appointment period begins due to variations in first class day. The appointment periods are as follow:<br />
	Fall, Sept 1 to Jan 15<br />
	Spring, Jan 16 to May 31<br />
	Summer I, June 1 to July 15<br />
	Summer II, July 15 to August 31<br />
	Entire Summer, June 1 - August 31<br /></p>

<h2>Responsibilities</h2>

<P>TA's can have a wide range of responsibilities depending on their assignment. TA's working in a graduate school are not to do grading of other graduate students. Responsibilities can include leading review sessions and holding office hours, to leading field trips, and generating course web sites and other instructional material. Because responsibilities range so widely, as soon as you are notified of your TA appointment, arrange to meet with your supervisor to make plans for the semester. </p>

<h2>Job Description and Expectations</h2>

<p>Request that your supervisor prepare a job description and discuss specifically with your supervisor their expectations for the semester. The chart on the other side of this document offers suggestions for discussing these expectations.</p>

