<h1>DIGITAL ARCHAEOLOGY LAB</h1>

<h2>Location</h2>

<p><a href="http://www.utexas.edu/maps/main/buildings/uta.html">UTA</a> 5.508 | <a href="http://www.ischool.utexas.edu/images/uta_fifth_floor_map.jpg">Fifth Floor Map</a></p>

<h2>Description</h2>

<p>The Digital Archaeology Lab is a significant part of the School of Information's research and education in digital archiving. The facility provides multiple computer platforms, including a complete forensic workstation, a variety of media drives, and current and legacy software for the purpose of recovering digital objects from noncurrent environments for transfer into a preservation environment. Students and faculty carry out research to develop extraction protocols and to test efficient and safe means of treating legacy media and formats, through projects working with significant digital collections on the School's DSpace institutional repository.</p>

<p>School of Information students and faculty are primary users of the lab, but the School partners with other UT institutions to address digital archiving problems on a regular basis.</p>

