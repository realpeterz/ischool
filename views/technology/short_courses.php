<h2>Boot Camp Web</h2>

<p>HTML and Templates (<a href="http://www.ischool.utexas.edu/technology/instruction/bootcamp_f08/final_html_handout_fall_2008.pdf">Handout</a> | <a href="http://www.ischool.utexas.edu/technology/instruction/bootcamp_f08/final_templates_html_fall_2008_slides.pdf">Slides</a>)<br />
		An introduction  to HTML  and templates. HTML is a means to describe the structure of text-based information in a document. Templates are a tool to separate the content from the presentation in Web design. Templates are  a quick and dirty way to get a Web site up fast; great for Web portfolios. <br />

<p>HTML and Dreamweaver (<a href="http://www.ischool.utexas.edu/technology/instruction/bootcamp_f08/final_dreamweaver_handout_fall_2008.pdf">Handout</a> | <a href="http://www.ischool.utexas.edu/technology/instruction/bootcamp_f08/final_dreamweaver_html_fall_2008_slides.pdf">Slides</a>)<br />
		An introduction  to HTML  and Adobe Dreamweaver. HTML is a means to describe the structure of text-based information in a document. Adobe Dreamweaver is a web design and site management program. Dreamweaver is an easy way to build sites without knowing a lot of code, and great for long-term site building and maintenance.</p>

<p>HTML and Intro to CSS (<a href="http://www.ischool.utexas.edu/technology/instruction/bootcamp_f08/css_bcw_fall_2008.pdf">Handout</a> | <a href="http://www.ischool.utexas.edu/technology/instruction/bootcamp_f08/final_css_html_fall_2008_slides.pdf">Slides</a>)<br />
		An introduction to HTML  and CSS. HTML is a means to describe the structure of text based information in a document. CSS is a stylesheet language used to describe the presentation of a document written in a markup language. CSS is a great way of changing the look of an entire site without having to change each page individually.</p>

