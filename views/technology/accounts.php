<p><a href="https://idmanager.its.utexas.edu/eid_self_help/">UT EID</a><br />
Students need an EID to register online, to view grades, to use Web email and to access a number of other critical services.</p>

<p><a href="http://www.utexas.edu/cc/account/">IF Account</a><br />
You need an IF Account to publish Web pages on UT servers, to gain access to computers in UT libraries and to use the Telesys ISP-service.</p>

