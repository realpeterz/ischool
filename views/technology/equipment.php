<h1>EQUIPMENT RESERVATION</h1> <!-- use h2 for all headings please -->

<p>The School of Information provides certain equipment for checkout to its students, faculty and staff. Equipment can be picked up and
dropped off during normal hours at the iSchool IT Lab (UTA 1.210). All equipment may be picked up after 2 PM and is due by noon on the due
date. Due to high demand, equipment renewals are not always possible. In order to request a renewal, bring the equipment to the IT Lab and
speak to an IT Lab staff member.</p>

