<h2>Tutorials and Short Courses</h2>

<p>Streaming media tutorials covering a vast array of tasks and applications are available in the
<a href="index.php?subsite=technology&page=tutorials" data-page="tutorials">Tutorials</a> section. Most of these tutorials consist
of quick videos you can watch online, clear instructions for browsing, or handouts you can print
and take with you. Tutorial handouts are also available in the IT Lab, along with short and sweet
one-page tutorials.</p>

<p>IT Services offers free classes on various computer topics for School of
Information students, faculty, and staff. Subjects range from operating
systems like Unix to instruction in image design and specific applications
such as Macromedia Dreamweaver. Check out our <a href="index.php?subsite=technology&page=short_courses" data-page="short_courses">Short Course</a>
page for the latest schedule and information on requesting a short course. The IT Lab Staff is
always interested in hearing your suggestions and is happy to work with students one-on-one or in groups.</p>

<h2>Connecting from home? Get BevoWare!</h2>

<p><a href="http://www.utexas.edu/its/bevoware/">BevoWare</a> provides the software you
need to protect your computer from viruses, improve the security of your connections, browse the
Web, read and send e-mail, use the campus printing system and more. Distributed by UT Austin
for both Macintosh and Windows computers, BevoWare saves you money, time and trouble.</p>

